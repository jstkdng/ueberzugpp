#ifndef __WINDOW__
#define __WINDOW__

#include <concepts>

class Window
{
public:
    virtual ~Window() = default;
    virtual void draw() = 0;
    virtual void generate_frame() = 0;
    virtual void show() {};
    virtual void hide() {};
};

template<class T>
concept WindowType = std::is_base_of<Window, T>::value;

#endif
